    private void btnsimpan4btnsimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsimpan4btnsimpanMouseClicked
        // TODO add your handling code here:
        String txtkd = txtkdpetugas.getText();
        String nama = txtnama.getText();
        String inisial = txtinisial.getText();
        String alamat = txtalamat.getText();
        String no_telp = txtnotelp.getText();
        String gender = null;

        if (jk_lk.isSelected()) {
            gender = "L";
        } else if (jk_p.isSelected()) {
            gender = "P";
        }

        try {
            Statement statement = (Statement) koneksi.GetConnection().createStatement();
            statement.executeUpdate("insert into petugas VALUES ('" + txtkd + "','" + nama + "','" + inisial + "','" + gender + "','" + alamat + "','" + no_telp + "','" + lblnmfile.getText() + "')");
            String path = new File(".").getCanonicalPath();
            FileUtils.copyFileToDirectory(file, new File(path + "/image"));
            statement.close();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal disimpan");
            System.out.println(e.getMessage());
        }
        datatablePetugas();
        kosongPetugas();
    }//GEN-LAST:event_btnsimpan4btnsimpanMouseClicked

