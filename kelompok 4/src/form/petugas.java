    public void datatablePetugas() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Kode Petugas");
        tbl.addColumn("Nama Lengkap");
        tbl.addColumn("Nama Inisial");
        tbl.addColumn("Gender");
        tbl.addColumn("Alamat");
        tbl.addColumn("NO Telpon");
        tbl.addColumn("Gambar");
        tblPetugas.setModel(tbl);
        try {
            Statement statement = (Statement) koneksi.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from petugas");
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("Id_Petugas"),
                    res.getString("Nama_Lengkap"),
                    res.getString("Nama_Inisial"),
                    res.getString("Gender"),
                    res.getString("alamat"),
                    res.getString("No_HP"),
                    res.getString("gambar")
                });
                tblPetugas.setModel(tbl);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "salah");
        }
    }
