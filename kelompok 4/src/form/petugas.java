    private void tblPetugasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPetugasMouseClicked
        // TODO add your handling code here:
        int i = tblPetugas.getSelectedRow();

        if (i >= 0) {
            txtkdpetugas.setText((String) tblPetugas.getValueAt(i, 0));
            txtnama.setText((String) tblPetugas.getValueAt(i, 1));
            txtinisial.setText((String) tblPetugas.getValueAt(i, 2));
            String gender = tblPetugas.getValueAt(i, 3).toString();
            if (gender.equals("L")) {
                jk_lk.setSelected(true);
            } else if (gender.equals("P")) {
                jk_p.setSelected(true);
            }
            txtalamat.setText((String) tblPetugas.getValueAt(i, 4));
            txtnotelp.setText((String) tblPetugas.getValueAt(i, 5));
            lblnmfile.setText((String) tblPetugas.getValueAt(i, 6));
            String gambar = tblPetugas.getValueAt(i, 6).toString();
            try {
                String path = new File(".").getCanonicalPath();
                Toolkit toolkit = Toolkit.getDefaultToolkit();
                Image image = toolkit.getImage(path + "/image/" + gambar);
                Image imagedResized = image.getScaledInstance(200, 250, Image.SCALE_DEFAULT);
                ImageIcon imageIcon = new ImageIcon(imagedResized);
                lblimage.setIcon(imageIcon);
            } catch (IOException ex) {
                Logger.getLogger(dokter.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_tblPetugasMouseClicked

