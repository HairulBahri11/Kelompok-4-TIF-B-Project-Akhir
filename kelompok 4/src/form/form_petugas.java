    private void btndeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btndeleteMouseClicked
        // TODO add your handling code here:
        String txtkd = txtkddokter.getText();
        try {
            Statement statement = (Statement) koneksi.GetConnection().createStatement();
            statement.executeUpdate("DELETE from mahasiswa where nim=('" + txtkd + "');");
            JOptionPane.showMessageDialog(null, "data berhasil dihapus");
            txtkddokter.setText("");
            txtnama.setText("");
            txtalamat.setText("");
            txtnotelp.setText("");
            txtkddokter.requestFocus();
        } catch (Exception t) {
            JOptionPane.showMessageDialog(null, " data gagall dihapus");
        }
    }//GEN-LAST:event_btndeleteMouseClicked

    private void btnsimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsimpanMouseClicked
        // TODO add your handling code here:
        String txtkd = txtkddokter.getText();
        String nama = txtnama.getText();
        String inisial = txtinisial.getText();
        String alamat = txtalamat.getText();
        String no_telp = txtnotelp.getText();
        String gender = null;
        
        if (jk_lk.isSelected()){
            gender = "L";
        }else if (jk_p.isSelected()){
            gender = "P";
        }

        try {
            Statement statement = (Statement) koneksi.GetConnection().createStatement();
            statement.executeUpdate("insert into dokter values ('" + txtkd + "','" + nama + "','" + inisial + "','" + gender + "','" + cmbspesialis.getSelectedItem() + "','" + alamat + "','" + no_telp + ")'");
            String path = new File(".").getCanonicalPath();
            FileUtils.copyFileToDirectory(file, new File(path+"/image"));
            statement.close();
            JOptionPane.showMessageDialog(null, "data berhasil disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal disimpan");
        }
        kosong();
    }//GEN-LAST:event_btnsimpanMouseClicked

    private void btnaddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnaddMouseClicked
        // TODO add your handling code here:
        jfc = new JFileChooser();
        if (jfc.showOpenDialog(lblimage) == JFileChooser.APPROVE_OPTION) {

            Toolkit toolkit = Toolkit.getDefaultToolkit();
            Image image = toolkit.getImage(jfc.getSelectedFile().getAbsolutePath());
            Image imagedResized = image.getScaledInstance(200, 250, Image.SCALE_DEFAULT);
            ImageIcon imageIcon = new ImageIcon(imagedResized);

            lblimage.setIcon(imageIcon);

            file = new File(jfc.getSelectedFile().getPath());
        }
    }//GEN-LAST:event_btnaddMouseClicked

