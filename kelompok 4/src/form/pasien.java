    public void datatabel() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Kode Pasien");
        tbl.addColumn("Nama Lengkap");
        tbl.addColumn("Nama Inisial");
        tbl.addColumn("Gender");
        tbl.addColumn("Tanggal Lahir");
        tbl.addColumn("Alamat");
        tbl.addColumn("Keluhan");
        tbl.addColumn("Poli Tujuan");

        tabel.setModel(tbl);

        try {
            Statement statement = (Statement) koneksi.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from pasien");
            while (res.next()) {
                tbl.addRow(new Object[]{
                    res.getString("Kode_Pasien"),
                    res.getString("Nama_Lengkap"),
                    res.getString("Nama_Inisial"),
                    res.getString("Gender"),
                    res.getString("Tanggal_Lahir"),
                    res.getString("Alamat"),
                    res.getString("Keluhan"),
                    res.getString("Poli_Tujuan")
                });
                tabel.setModel(tbl);
            }
        } catch (Exception e) {
            System.out.println("Salah");
        }
    }

